        2011  => '获取文章列表失败, errinfo',
        2012  => '删除数据失败',
    );

    public static function get($code){
        if(isset(self::ERRMAP[$code])){
            return array('errno'=>(0-$code), 'errmsg'=>self::ERRMAP[$code]);
        }
        return array('errno'=>(0-$code), 'errmsg'=>"没有定义该类错误码");

    }
}
