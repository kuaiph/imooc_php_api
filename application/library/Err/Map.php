class Err_Map{
    const ERRMAP = array(
        0     => '',
        /**
         * 用户类错误提示码
         */
        1001  => '请通过正常渠道提交',
        1002  => '用户名或密码不能为空',
        1003  => '用户查找失败',
        1004  => '密码错误',
        1005  => '用户名已存在',
        1006  => '密码太短，请输入最低8位的密码',
        1007  => '注册失败，写入数据库失败',
        /**
         * 文章类错误提示码
         *
         */
        2000  => '需要管理员权限',
        2001  => '请通过正常渠道提交',
        2002  => '没填写完整',
        2003  => '缺少必要的参数',
